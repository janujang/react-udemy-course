  //     setFormIsValid(
  //       enteredEmail.includes("@") && enteredPassword.trim().length > 6
  //     );
  //   }, 500); //execute after 500ms of keystroke

  //clears the timer if typing very fast so the form isn't checked for validity
  //clean up function runs before every new side effect execution
